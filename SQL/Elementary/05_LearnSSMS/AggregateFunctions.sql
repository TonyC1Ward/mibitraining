USE [TSQLV4];
GO

SELECT *
FROM [stats].[Scores]

------------------------------------------------------------------------------------------------------------------------------------------------------------
-- SUM - Adds up all scores together (can only be performed on int)
------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT SUM(score) AS TotalOfAllScores
FROM [Stats].[Scores]

------------------------------------------------------------------------------------------------------------------------------------------------------------
-- COUNT - Counts the amount of records in the Studentid column
------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT COUNT(Studentid) AS CountOfStudents
FROM [stats].Scores

------------------------------------------------------------------------------------------------------------------------------------------------------------
-- MIN - outputs the lowest record in the score column
------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT MIN(score) AS LowestScore
FROM [stats].[Scores]

------------------------------------------------------------------------------------------------------------------------------------------------------------
-- MAX - outputs the highest record in the score column
------------------------------------------------------------------------------------------------------------------------------------------------------------
SELECT MAX(score) AS HighestScore
FROM [Stats].[Scores]

